<h3>A Simple Model</h3>
<p>To calculate how much strictly functional human DNA has not already been
	destroyed, suppose the following:</p>
<ol>
	<li>25 years per human generation.</li>
	<li>3 million functional units of 1000 <dfn>nucleotides</dfn> each, making a 3 billion
		DNA-letter genome.&nbsp; Humans have two copies of each gene, so alternatively that would
		make 6 billion if we count both copies of each gene.</li>
	<li>50 haploid (affecting one only set of genes) mutations per generation.&nbsp; The total
		mutation rate is 100 mutations per generation,<span>[^moran-2013] so we divide
		that by two.</span></li>
	<li>45 haploid mutations that persist from one generationto the next, assuming 5 are removed by
		natural selection.[^gibson-2011]</li>
	<li>A 10% chance a mutation within a gene will destroy that gene.&nbsp; Many mutations are
		neutral--having no effect on function, and others only slightly degrade the function of a
		gene.</li>
	<li>Most mutations are either neutral or only slightly
		<dfn>deleterious</dfn>,[^eyre-walker-2007] [^lind-2010] causing genes
		to slowly degrade across the entire population.&nbsp; Then when a gene becomes entirely
		non-functional, selection is largely blind to the difference between it and a hightly
		degraded version of the same gene.</li>
</ol>
<p>Multiplying the 45 haploid mutations times the 10% chance a mutaiton will destroy a gene gives
