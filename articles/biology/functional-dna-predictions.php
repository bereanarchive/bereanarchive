<h3>3. Selfish genes</h3>
<p>Some sequences of DNA, called transposons or mobile-elements, have the ability to make copies of
	themselves within DNA.&nbsp; Transposons range in size from a few hundred to tens of
	thousands of letters of DNA.[^britannica-transposon]&nbsp; The selfish
	gene theory proposed that among such sequences, those best at copying themselves will
	produce more copies of themselves than those that copy less frequently.</p>
<p>Most complex organisms have hundreds of millions to billions of letters of DNA. Since having
	slightly more DNA has a negligible effect on one's ability to survive and reproduce, natural
	selection does not differentiate between organisms where transposons have produced several more
	copies.&nbsp; Since "only very precise deletion can be non-lethal,"[^doolittle-1980] these
	copying sequences spread much faster than they are removed.&nbsp; Because of this process,
	proponents of evolutionary theory proposed that the bulk of large genomes are made up of such
	selfish genes:</p>
