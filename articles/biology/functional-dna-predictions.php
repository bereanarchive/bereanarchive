<h3>2. Genetic load</h3>
<p>Genetic load (also called mutational load) is the average number of <dfn>deleterious</dfn>
	(harmful) mutations per organism in a population.&nbsp; If the genetic load is too high, the
	population will not survive.<br></p>
<p>Organisms with more DNA genreally have more mutations, since the number of errors increases as
	more is copied.&nbsp; If most of the DNA in large genome organisms (e.g. mammals) is functional,
	then these mutations will usually break important functions.&nbsp; And if evolution could not
	even maintain these large amounts of DNA, then it could not have created it.</p>
<p>But if most DNA does nothing, or does not have specific information, then most mutations will be
	harmless.&nbsp; Therefore because of this genetic load problem, evolutionary theory both
	predicts and requires only a small amount of DNA in large genome organisms will be functional.
	</p>
