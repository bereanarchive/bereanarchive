<h3>Caveats with These Models</h3>
<ol>
	<li>The results become more skewed further into the past, since when only 66% of the genome is
		functional, the deleterious mutation rate is reducted to 50 times 66%.</li>
	<li>Genes work together in complex, interacting networks.&nbsp; Knocking out one pair of genes
		could make the entire network non-functional.&nbsp; Conversely, unrelated genes may kick in
		to perform the same task as other genes that have failed.&nbsp; This model accounts for
		neither phenomenon.</li>
</ol>
<h3>Other Models</h3>
<p>Botanist Alex Williams makes a more pessimistic
	estimate in a 2008 Journal of Creation article.&nbsp; Based on estimated rates of gene loss and the percentage of critical
	genes, Williams calculates between 1200 and 1.5 million until humans accumulate too
	many mutations to survive.[^williams-2008]</p>
<p>However, a less-involved form of design could give very different predictions. Perhaps a
	designer only seeded the first cells on earth, or that chemistry itself was designed to greatly
	increase the odds of life forming from non-living matter?&nbsp; And then unguided evolution
	proceeded from there to create most of the life forms we see today.&nbsp; If a designer only
	played such a modest role, then the bulk of larger genomes could be junk.</p>
