<p>The following describes the fascinating history of this debate. </p>
<h2>Proponents of Evolutionary Theory Predicted Junk DNA</h2>
<p>There were at least six reasons why proponents of evolutionary theory predicted most DNA in
	large genomes would be junk, four of which are based on evolutionary theory itself.&nbsp; In
	other words, if evolutionary theory is true these are the reasons we should expect that most DNA
	in large genomes is junk:</p>
<h3>1. Neutral theory</h3>
<p>The neutral theory of evolution claims that most of the DNA differences between larger genome
	organisms is by chance, and not because natural selection favored organisms having those
	mutations.&nbsp; The reasoning is as follows:&nbsp; </p>
<ol>
	<li><span>In typical populations of complex organisms, natural selection can only help spread
		or remove mutations that have a strong effect.&nbsp; Otherwise, random chance is the
		dominant factor in deciding which organisms reproduce and pass on their genes.</span></li>
	<li>Since most mutations have very little effect on an organism,[^eyre-walker-2007] [^lind-2010] neutral theory is a
		mathematical reality.[^lynch-2006]"[T]he revolution is over. Neutral and nearly neutral theory
		won,"as biologist PZ Myers described.[^myers-2014]
	</li>
</ol>
<p>In humans and other large mammals, greater than 99.98% of mutations would spread through the
	population by chance rather than because of natural selection.[^dodson-1962] [^moran-2014b]
	</p>
<p>Only about 5 to 10% of DNA is shared between humans and more distantly related mammals such as
	horses, dogs, and mice.[^meader-2010]&nbsp; Therefore if these animals all evolved from a
	common ancestor, given neutral theory, the large majority of DNA not shared by these animals
	would have come about by chance and not natural selection.&nbsp; Since DNA that exists only
	by chance will have a random sequence, it is therefore highly unlikely to be functional.
	</p>
