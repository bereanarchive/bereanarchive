<p>Above, Richard Dawkins clarifies that he is only talking about the illusion
	of design.&nbsp; He believes that evolution is an adequate mechanism to account
	for it, and bad designs exist in biology that are contrary to what a designer would
	create.&nbsp; Many others in this list would agree.&nbsp; But if those claims can
	be shown meritless (as other articles on the Berean Archive argue) then we're left
	with the appearance of design and no other explanation for it.</p>
<h2>Rejecting the appearance of design</h2>
<p>
	However some biologists reject the appearance of design:</p>
<table class="quotes" style="width: 100%;">
	<tbody>
		<tr>
			<td style="width: 20%;">
				<img src="/common/img/portraits/francisco-ayala.jpg?w=150">
			</td>
			<td>
				<p><b>Francisco Ayala</b>
					<br>Evolutionary biologist, former AAAS president, intelligent design critic, Christian[^ayala-photo]</p>
				<blockquote>
					<p>But the design of organisms is not intelligent, as it would be expected from
						an engineer, but imperfect and worse: defects, dysfunctions, oddities, waste,
						and cruelty pervade the living world.[^ayala-1994]</p>
				</blockquote>
			</td>
		</tr>
		<tr>
			<td style="width: 20%;">
				<img src="/common/img/portraits/larry-moran.jpg?w=150">
			</td>
			<td>
				<p><b>Larry Moran</b><br>Biochemist, textbook author, intelligent design critic, atheist[^moran-photo]
				</p>
				<blockquote>
					<p>Most of the genes that code for complex structures are inefficient and error-prone.
						Natural systems look like they were designed by a tinkerer who cobbles together
						odds and ends that just happened to be in reach. They look like they evolved
						haphazardly. Any intelligent scientist could do better and, in some cases,
						they have done better by genetically modifying organisms to make them more
						efficient.[^moran-bad-design]
					</p>
				</blockquote>
			</td>
		</tr>
	</tbody>
</table>
<p>Yet if evolutionary processes destroy faster than they create, it is expected that
	some designs will become degraded and broken.</p>
