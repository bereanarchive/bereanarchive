 <h2>Argument 3:&nbsp; "Evolution doesn't include abiogenesis"</h2>
<p>The distinction is rather arbitrary and there are those who see it both ways.
	For example see evolutionary biologist Eugene Koonin in The Logic of Chance,
	2012, <a href="http://books.google.com/books?id=fvmv2kU6PrYC&amp;q=%22In%20my%20view%2C%20all%20advances%22">page 417</a>:</p>
<blockquote>
	<p>In my view, all advances notwithstanding, evolutionary biology is and will remain
		woefully incomplete until there is at least a plausible, even if not compelling,
		origin of life scenario.</p>
</blockquote>
<p>Prominent ID critic and biologist PZ Myers <a href="http://scienceblogs.com/pharyngula/2008/02/20/15-misconceptions-about-evolut/">blogged in 2008</a>:</p>
<blockquote>
	<p>I know many people like to recite the mantra that “abiogenesis is not evolution,”
		but it’s a cop-out. Evolution is about a plurality of natural mechanisms that generate
		diversity. It includes molecular biases towards certain solutions and chance events
		that set up potential change as well as selection that refines existing variation.
		Abiogenesis research proposes similar principles that led to early chemical evolution.
		Tossing that work into a special-case ghetto that exempts you from explaining it
		is cheating, and ignores the fact that life is chemistry. That creationists don’t
		understand that either is not a reason for us to avoid it.</p>
</blockquote>
<p>Although this is no more than a debate over the scope of terminology.&nbsp; Whether
	or no abiogenesis is true doesn't depend on whether it is or isn't considered
	"part of evolutionary theory."</p>
 <h2>Argument 4:&nbsp; "Evolutionary theory is critical to our understanding of biology"</h2>
<p>Evolutionary biologist Theodosius Dobzansky is famous (among other reasons) for his
	<a href="http://people.delphiforums.com/lordorman/Dobzhansky.pdf">1973 essay</a> titled
	"Nothing in biology makes sense except in light of evolution."&nbsp; Mutation and
	selection certainly are very important and nobody (not even creationists) are arguing
	they should be abandoned.&nbsp; However, the idea that all life evolved from
	a common ancestor through unguided processes is superfluous to our understanding
	of biology. Here I provide a sampling of supporting perspectives:</p>
<p>Philip Skell was a chemist and co-inventor of penicillin.&nbsp; In 2005 he published
	<a href="http://classic.the-scientist.com/2005/08/29/10/1/">Why Do We Invoke Darwin</a> in
	The Scientist, arguing evolutionary theory was unnecessary for biology:</p>
<blockquote>
	<p>Certainly, my own research with antibiotics during World War II received no guidance
		from insights provided by Darwinian evolution. Nor did Alexander Fleming's discovery
		of bacterial inhibition by penicillin. I recently asked more than 70 eminent researchers
		if they would have done their work differently if they had thought Darwin's theory
		was wrong. The responses were all the same: No. ... When an explanation is so supple
		that it can explain any behavior, it is difficult to test it experimentally, much
		less use it as a catalyst for scientific discovery. ... Darwinian evolution – whatever
		its other virtues – does not provide a fruitful heuristic in experimental biology.
		This becomes especially clear when we compare it with a heuristic framework such
		as the atomic model, which opens up structural chemistry and leads to advances
		in the synthesis of a multitude of new molecules of practical benefit. None of
		this demonstrates that Darwinism is false. It does, however, mean that the claim
		that it is the cornerstone of modern experimental biology will be met with quiet
		skepticism from a growing number of scientists in fields where theories actually
		do serve as cornerstones for tangible breakthroughs.</p>
</blockquote>
<p>Skell was the first signer of the Discovery Institute's <a href="http://www.dissentfromdarwin.org/">Dissent from Darwin</a>.</p>
<p>Also in 2005, Marc Kirschner (chair of systems biology at Harvard Medical) shared
	similar sentiments in <a href="http://archive.truthout.org/article/missing-links">an article</a> in
	the Boston Globe:</p>
<blockquote>
	<p>Kirschner likes to invoke the much-quoted declaration of famed 20th-century biologist
		Theodesius Dobzhansky that 'nothing in biology makes sense except in the light
		of evolution' (the title of a 1973 essay).&nbsp; 'In fact, over the last 100 years,
		almost all of biology has proceeded independent of evolution, except evolutionary
		biology itself,' Kirschner declares. 'Molecular biology, biochemistry, physiology,
		have not taken evolution into account at all.'</p>
</blockquote>
<p>Jerry Coyne is an evolutionary biologist at the University of Chicago, and one of
	the most ardent proponents of evolutionary theory.&nbsp; But he also agrees that
	evolutionary theory is lacking in providing practical benefits.&nbsp; In
	<a href="http://ge.tt/8P6o5lt/v/0">Selling Darwin</a> (Nature, 2006) he reviews
	the book <em>The Evolving World</em> by David Mindell:</p>
<blockquote>
	<p>In his desire to show how useful evolution is, Mindell strives desperately
		to herd every stray area of biology, even those barely related to evolution,
		into the darwinian fold... </p>
	<p>To some extend these excesses are not Mindell's fault, for, if truth be told, evolution
		hasn't yielded many practical or commercial benefits. Yes, bacteria evolve drug
		resistance, and yes, we must take countermeasures, but beyond that there is not
		much to say. Evolution cannot help us predict what new vaccines to manufacture
		because microbes evolve unpredictably. But hasn't evolution helped guide animal
		and plant breeding? Not very much. Most improvement in crop plants and animals
		occurred long before we knew anything about evolution, and came about by people
		following the genetic principle of "like begets like". Even now, as its practitioners
		admit, the field of quantitative genetics has been of little value in helping improve
		varieties. Future advances will almost certainly come from transgenics, which is
		not based on evolution at all. </p>
	<p>As far as I know, there have been only two genuine commercial applications of evolutionary
		theory. One is the use of "directed evolution" to produce commercial products (such
		as enzymes to protect crop plants from herbicides). The other is the clever use
		of insecticide-free "pest refuges" to stop herbivorous insects evolving resistance
		to herbicides containing Bacillus thuringiensis (Bt) toxins, a strategy derived
		from principles of population genetics. There will certainly be more of these to
		come. And evolutionary algorithms are used in designing computer programs, and
		may have uses in engineering and economics.</p>
</blockquote>
<p>Likewise, in 2000 Coyne lamented <a href="http://www.uic.edu/labs/igic/papers/others/Coyne_2000.pdf">in a book review</a>:</p>
<blockquote>
	<p>In science's pecking order, evolutionary biology lurks somewhere near the bottom, far closer to phrenologythan
		to physics. For evolutionary biology is a historical science, laden with history's
		inevitable imponderables. We evolutionary biologists cannot generate a Cretaceous
		Park to observe exactly what killed the dinosaurs; and, unlike "harder" scientists,
		we usually cannot resolve issues with a simple experiment, such as adding
		tube A to tube B and noting the color of the mixture.</p>
</blockquote>
<p>Francis Crick was the co-discoverer of the structure of DNA and an atheist.&nbsp; He
	likewise wrote:
	<br>
</p>
<blockquote>
	<p>It might be thought, therefore, that evolutionary arguments would play a large part
		in guiding biological research, but this is far from the case.&nbsp; It is difficult
		enough to study what is happening right now.&nbsp; To try to figure out exactly
		what happened in evolution is even more difficult.&nbsp; Thus evolutionary arguments
		can be used as hints to suggest possible lines of research, but it is highly dangerous
		to trust them too much.[^crick-mad-pursuit]</p>
</blockquote>
 <h2>Argument 5:&nbsp; "It's impossible to understand evolutionary theory without accepting it"</h2>
<p>In 2014, creationist and evolutionary biologist Todd Wood reported his undergraduate
	biology major students <a href="http://toddcwood.blogspot.com/2014/03/you-are-my-glory-and-joy.html">scored in the top 99th</a> percentile
	on standardized evolutionary biology tests.&nbsp; As he clarified, "The 99th percentile
	means they're outperforming most students taught by actual evolutionists."</p>
<p>Todd Wood appears to be talking about the <a href="https://web.archive.org/web/20130301205514/http://www.ets.org/gre/subject/about/content/biology">Biology GRE</a>.
	&nbsp;He said "The test splits up their scores in four categories: cell,
	organismal, genetics, and evolution".&nbsp; The Biology GRE is technically split
	into three categories, but the first is divided into two categories which are totalled
	separately, which then matches Wood's categories.</p>
<h2>Argument 6:&nbsp; "Humans are apes, we didn't evolve from apes"</h2>
<p>The classification is arbitrary.&nbsp; See <a href="http://johnhawks.net/weblog/topics/phylogeny/taxonomy/humans-arent-apes-2012.html">Humans aren't monkeys. We aren't apes either</a>,
	by anthroplogist John Hawks:</p>
<blockquote>
	<p>Jerry [Coyne] is far from alone in this -- many evolutionary biologists state as
		if it were an unquestionable fact that humans are apes. I disagree...</p>
	<p>"Ape" is an English word. It is not a taxonomic term. English words do not need
		to be monophyletic. French, German, Russian, and other languages do not have to
		accord with English ways of splitting up animals. Taxonomy is international --
		everywhere, we recognize that humans are hominoids.&nbsp; In French, apes are singes.
		So are monkeys. In English we differentiate these terms. In both languages humans
		are different from other primates. Does that mean French is right and English wrong?
		Does it mean both languages are wrong?</p>
	<p>No, it means that colloquial languages have no problem describing paraphyletic groups.
		It is useful to have languages that can make these distinctions.&nbsp; If we must
		accept that humans are apes, then we must equally accept that chimpanzees are monkeys,
		and those awful parents at the zoo are right.</p>
</blockquote>
<p>Biological anthropologist Jonathan Marks <a href="http://popanth.com/article/are-we-apes-no-we-are-humans">makes similar comments</a>:</p>
<blockquote>
	<p>Science no more says that I am an ape because my ancestors were, than it says that
		I am a slave because my ancestors were. The statement that you are your ancestors
		articulates a bio-political fact, not a biological fact. And it is ridiculous and
		offensive in the modern era, in addition to being false.&nbsp; What are we? We
		are human... On the other hand, we also fall phylogenetically within the group that we call "fish."... Yet
		we are not fish. There are certainly things to be understood by confronting our
		fish ancestry (such as our gestation in a saline, aqueous environment), but fish
		can't read, so if you are reading this, then you are not a fish.</p>
</blockquote>
 <h2>Argument 7:&nbsp; "Scientists who are religiously motivated can't be trusted"</h2>
<p>Should we dismiss Isaac Newton's work for the same reason?&nbsp; Newton wrote:
</p>
<blockquote>
	<p>I have a fundamental belief in the bible as the word of God, written by men who
		were inspired.&nbsp; I study the bible daily ... all my discoveries have been made
		in answer to prayer</p>
</blockquote>
<p>What would happen if we applied the same standard in reverse?&nbsp; Eugenie Scott
	is former president of the National Center for Science Education, which is an
	activist group fighting to ensure evolutionary theory is the only origins theory
	taught. She was a <a href="http://americanhumanist.org/Humanism/Humanist_Manifesto_III/Notable_Signers">signer of the humanist manifesto</a>--the
	core document of a secular religion, but it would be unjust to dismiss her arguments
	because of that.</p>
 <h2>Argument 8:&nbsp; "Creation and ID proponents are only in it for the money"</h2>
<p>In the American Spectator, journalist and ID critic John Derbyshire <a href="http://spectator.org/articles/57159/does-intelligent-design-provide-plausible-account-lifes-origins?page=2">defends ID proponents</a> against
	this accusation:</p>
<blockquote>
	<p>The least charitable skeptics accuse ID promoters of running a racket, taking part
		in the grand old American tradition of fleecing the rubes. (As the immortal Al
		Bundy told his acolytes while winding up for his sermon at the Church of NO MA’AM:
		“Now it’s time to eece-flay the ongregation-cay.”) I’m a cynic, but not that much
		of a cynic. I have engaged in formal debate on Intelligent Design on three or four
		occasions. I once spent an hour in a room full of principals from the Discovery
		Institute (DI). They struck me as persons who believe in what they are selling.
		The Charity Navigator website lists their total 2011 revenues as $5.7 million,
		which is not a lot. The executives, according to that same website, are not extravagantly
		paid.
	</p>
</blockquote>
<p>In his <a href="http://www.bryan.edu/assets/files/Podcasts/Chapel/2014/20140205%20Chapel%20-%20Christopher%20Yuan.mp3">dialog</a> with
	biologist and BioLogos president Darrel Falk (starting at 17:57), Young earth creationist
	and evolutionary biologist Todd Wood comments on the lousy pay of his line of work:</p>
<blockquote>
	<p>I just love science and think it's amazing.&nbsp; The opportunity to study God's
		handiwork is amazing.&nbsp; The opportunity to take the bible in one hand and read
		about the flood and the tower of babel and the garden of Eden and take science
		in the other hand and see how they mesh together is astonishingly fun.&nbsp; And
		if you want a fun career then my career is awesome.&nbsp; It pays terribly but
		my career is totally awesome.</p>
</blockquote>
<p>Darrel Falk doesn't challenge Wood's statement and continues the discussion amicably.
	&nbsp;However in <a href="http://biologos.org/blog/a-rejoinder-to-meyer-2">a response</a> to
	ID proponent Stephen Meyer, Falk shares that although he disagrees he considers
	intelligent design a legitimate science:</p>
<blockquote>
	<p>First, I would like to note that one cannot help but be impressed with how current
		Meyer is with regard to this literature. He cites ten articles from 2009 and three
		from this year. Clearly he and his colleagues are keeping up with the literature.
		This is just one example of why I have come to conclude that the ID movement ought
		to be considered a scientific movement.&nbsp; Another example is Meyer’s book itself.
		In the book, Meyer, quite eloquently in my opinion, demonstrates that his approach
		fits within the bounds of how historical science is done. The fact that he spends
		time carefully analyzing the scientific literature for evidence of function is
		further indication that they are doing science. Their Intelligent Design model
		predicts that the DNA in the human genome (and other organisms) is fully functional,
		and Meyer and his colleagues are carefully scrutinizing the scientific literature
		to see if it is.</p>
</blockquote>
<p>If ID proponents were pushing their views for illigitimate reasons, then critics
	like John Derbyshire and Darrel Falk would be the first to call them out on it.
	&nbsp;Rather, they say the opposite.&nbsp; Even Nick Matzke, who spends much of
	his time fighting against creation and design, <a href="http://pandasthumb.org/archives/2007/10/full-text-of-th.html#comment-133519">agrees that</a> "Creationists
	usually believe what they say."</p>
