	the lack of interest in apologetics in general:</p>
<blockquote>
	<p>If you are interested in apologetics or are already leading an apologetics group
		in your church, you probably already know how unpopular the topic can be. Apologetics
		groups are typically the smallest groups in the Church. Apologetics books sell
		less than other Christian topics, and apologetics conferences are less well attended
		than other types of Christian seminars.</p>
</blockquote>
