<h1>Low-Effort Arguments in the Evolution Debate</h1>


 <h2>Overview</h2>
<p>This article addresses simpler arguments often brought against intelligent design
	and its proponents.&nbsp; These are typically over terminology, arbitrary distinctions,
	or involving a genetic fallacy that mostly distract from the real origins debate.
	&nbsp;But since they come up frequently it's still useful to address them.&nbsp; Also
	see <a href="http://www.uncommondescent.com/faq/">Frequently raised but weak arguments against ID</a> at
	Uncommon Descent.
</p>
<p></p>
 <h2>Argument 1:&nbsp; "Real scientists don't use the terms micro and macroevolution"</h2>
<p>There's also the common variant, "micro and macro evolution are terms invented by
	creationists."&nbsp; I can see how the terms aren't well defined, but the difference
	is recognized by many well known biologists.</p>
<h3>Micro- and Macroevolution are legitimate terms used by biologists</h3>
<p>Evolution.berkeley.edu defines <em>macroevolution</em> as "evolution on a grand scale--what
	we see when we look at the over-arching history of life: stability, change, lineages
	arising, and extinction."[^berkeley-macroevolution]</p>
<p>Some even include the word macroevolution as part of their PhD title.&nbsp; E.g.
	Jon Tennant is currently undertaking a PhD in vertebrate macroevolution."[^john-tennant]</p>
 <h3>It's common for evolutionary biologists to debate whether repeated microevolution
		can lead to macro</h3>


<p>Roger Lewin describes the controversy at a 1980 evolutionary biology conference in Chicago:</p>
<blockquote>
The central question of the Chicago conference was whether the mechanisms underlying microevolution can be extrapolated to explain the phenomena of macroevolution.  At 
the risk of doing violence to the positions of some of the people at the meeting, the answer can be given as a clear, No.  What is not clear, however, is whether microevolution is totally decoupled from macroevolution:  The two can more probably be seen as a continuum with a notable overlap."[^lewin-1980]
</blockquote>
<p>Biologist Andrew M. Simons writes:</p>
<blockquote>
	<p>A persistent debate in evolutionary biology is one over the continuity of microevolution
		and macroevolution--whether macroevolutionary trends are governed by the principles
		of microevolution.[^simons-continuity]</p>
</blockquote>
<p>In 2001, the European Molecular Biology Organization issued a report summarizing
	a meeting where PhD students debated whether extrapolation from microevolution
	to macroevolution was justified:</p>
<blockquote>The symposium ended with a panel discussion about questions of microevolution (evolution
	within the species) and macroevolution (evolution after speciation).&nbsp; The issue
	at stake was whether extrapolation from the selection theory operating on organisms
	is sufficient to explain all patterns of macroevolution.&nbsp; In other words, do
	we need an independent body of theory to explain the changes occurring above, as
	opposed to at, the species level?&nbsp; There was no general agreement among the
	panel members.&nbsp; It seems that the jury is still out on this important question.
	... Many speakers emphasised the role of internal constraints, which had not been
	considered in conventional Darwinian thinking. Constraints set, for example, by
	developmental gene networks, and probably many other unforeseen rules of complexity,
	define the boundaries of what is possible.[^embo-macroevolution]</blockquote>
<p>Michael R. Dietrich says he is among only a minority of biologists who say no distinction
	should be made betwen micro and macroevolution:</p>
<blockquote>
	<p>Most scientists would accept that there are distinct phenomena that can be categorized
		as microevolutionary and macroevolutionary.[^dietrich-debates]
	</p>
</blockquote>
<p>Likewise, In arguing for an erasure between the lines of micro and macro, biologist
	Sean B. Carol first concedes there's no agreement on the matter:</p>
<blockquote>
	<p>A long-standing issue in evolutionary biology is whether the processes observable
		in extant populations and species (microevolution) are sufficient to account for
		the larger-scale changes evident over longer periods of life's history (macroevolution).
		&nbsp;Outsiders to this rich literature may be surprised that there is no consensus
		on this issue, and that strong viewpoints are held at both ends of the spectrum,
		with many undecided.[^carol-big-picture]
	</p>
</blockquote>
<p>A 2016 biology textbook similarly notes:</p>
<blockquote>
	<p>The history of biodiversity—the processes and patterns of originations, adaptations,
		and extinctions—is known as macroevolution.&nbsp; The term stands in contrast to
		microevolution, the change of allele frequences within populations caused by mechanisms
		such as genetic drift and natural selection.[^zimmer-macroevolution]</p>
</blockquote>
<h3>Many recognize macroevolution must entail more than long-term microevolution</h3>

<p>Evolutionary developmental biologist Eric Davidson wrote that changes in developmental
	networks are fundamentally different than small changes and species divergences:</p>
<blockquote>
	<p>contrary to classical evolution theory, the processes that drive the small changes
		observed as species diverge cannot be taken as models for the evolution of the
		body plans of animals. These are apples and oranges.[^davidson-regulatory]</p>
</blockquote>
<p>Molecular and cell biologist Richard Strohman discusses how the fossil record shows
	large jumps instead of the gradual accumulations expected by microevolution:</p>
<blockquote>
	<p>There is a striking lack of correspondence between genetic and evolutionary change.
		&nbsp;Neo-Darwinian theory predicts a steady, slow continuous, accumulation of
		mutations that produces a progressive change in morphology leading to new species,
		genera, and so on.&nbsp; But macroevolution now appears to be full of discontinuities,
		so we have a mismatch of some importance.&nbsp; That is, the fossil record shows
		mostly stasis, or lack of change, in a species for many millions of years; there
		is no evidence there for gradual change even though, in theory, there must be a
		gradual accumulation of mutations at the micro level.[^strohman-khunian-revolution]</p>
</blockquote>
<p>Likewise, paleontologist and ID critic Donald Prothero describes how the
	changes seen among Galapagos Finches are very different than the large jumps seen
	in the fossil record:</p>
<blockquote>
	<p>The gradual adaptation of fruit flies and Galapagos finches may be good examples
		of short-term microevolutionary change, but they simply do not address what the
		fossil record has shown for over a century. This only became apparent when
		Gould, Eldredge, Stanley and others began to talk about species sorting, decoupling
		microevolution from macroevolution, and the importance of hierarchical thinking
		in evolutionary biology. Many neontologists appear to maintain a relentlessly reductionist
		attitude for no apparent reason.[^prothero-high-table]</p>
</blockquote>
<p>The well known paleontologists Douglas Erwin and James Valentine made a similar distinction
	in their 2013 book on the Cambrian Explosion:</p>
<blockquote>
	<p>One important concern has been whether the microevolutionary patterns commonly studied
		in modern organisms by evolutionary biologists are sufficient to understand and
		explain the events of the Cambrian or whether evolutionary theory needs to be expanded
		to include a more diverse set of macroevolutionary processes. We strongly hold
		to the latter position.[^erwin-cambrian]</p>
</blockquote>
<p>In <em>Macroevolution is more than repeated rounds of microevolution</em>, Douglas
	Erwin made the same argument:</p>
<blockquote>
	Arguments over macroevolution versus microevolution have waxed and waned through
	most of the twentieth century... These discontinuities impose a hierarchical structure
	to evolution and discredit any smooth extrapolation from allelic substitutions to
	large-scale evolutionary patterns. ... The attractiveness of macroevolution reflects
	the exhaustive documentation of large-scale patterns which reveal a richness to
	evolution unexplained by microevolution.[^erwin-repeated-rounds]</blockquote>
<p>Nick Matzke is an evolutionary biologist&nbsp; and ID critic who is very active
	in the origins debate.&nbsp; He commented that those who deny
	a distinction aren't "very deep in the field":</p>
<blockquote>
	<p>Wikipedia articles can have a great many writers. The line “Contrary to claims by
		creationists, macro and microevolution describe fundamentally identical processes
		on different time scales” looks like it was stuck in by someone who was a popular
		fan of evolution but not very deep into the field.[^matzke-many-writers]</p>
</blockquote>
