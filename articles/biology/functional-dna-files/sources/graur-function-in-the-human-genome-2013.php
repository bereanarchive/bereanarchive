<p>Comment on:</p>
<h1>"Function" in the human genome according to the evolution-free gospel of ENCODE</h1>
<p>Dan Graur et al, Genome Biology and Evolution, 2013
	<br><a href="http://gbe.oxfordjournals.org/content/early/2013/02/20/gbe.evt028.full.pdf">Source</a> |
	<a href="http://web.archive.org/web/20130228155523/http://gbe.oxfordjournals.org/content/early/2013/02/20/gbe.evt028.full.pdf"
	>Archive.org</a>
</p>
<p>
	<a href="http://gbe.oxfordjournals.org/content/early/2013/02/20/gbe.evt028.full.pdf"
	></a>
</p>
<p>Dan Graur is an evolutionary and molecular biologist.&nbsp; In 2013 he and several
	others published a response to the 2012 results from the ENCODE team that
	&gt; 80% of the genome is functional.&nbsp; They argued "progress in understanding
	the functional significance of DNA sequences can only be achieved by not ignoring
	evolutionary principles", although also highlighting signatures of function
	found by ENCODE that could be explained by other means.</p>
<h2>Neutral Theory and Genetic Load</h2>
<blockquote>
	<p>[The ENCODE consortium] put forward the idea that more than 80% of the human
		genome is functional. This claim flies in the face of current estimates according
		to which the fraction of the genome that is evolutionarily conserved through
		purifying selection is under 10%. Thus, according to the ENCODE Consortium,
		a biological function can be maintained indefinitely without selection, which
		implies that at least 80 – 10 = 70% of the genome is perfectly invulnerable to deleterious
		mutations, either because no mutation can ever occur in these “functional” regions,
		or because no mutation in these regions can ever be deleterious.&nbsp; This
		absurd conclusion was reached through various means.... </p>
	<p>only sequences that can be shown to be under selection can be claimed with
		any degree of confidence to be functional.... The absurd alternative, which unfortunately was
		adopted by ENCODE, is to assume that no deleterious mutations can ever occur in the
		regions they have deemed to be functional. Such an assumption is akin to claiming that
		a television set left on and unattended will still be in working condition after
		a million years because no natural events, such as rust, erosion, static electricity,
		and earthquakes can affect it.</p>
	<p><b>there exists a misconception among functional genomicists that the evolutionary process can produce a genome that is mostly functional</b>.
		Actually, evolution can only produce a genome devoid of “junk” if and only if the
		effective population size is huge and the deleterious effects of increasing
		genome size are considerable... <b>In humans, there seems to be no selection against excess genomic baggage.</b> Our effective
		population size is pitiful and DNA replication does not correlate with genome size. </p>
</blockquote>
<h2>Selfish Genes</h2>
<blockquote>
	<p>Whether transcribed or not, the vast majority of transposons in the human genome
		are merely parasites, parasites of parasites, and dead parasites, whose main
		“function” would appear to be causing frameshifts in reading frames, disabling
		RNA-specifying sequences, and simply littering the genome.</p>
</blockquote>
<h2>Trial and Error</h2>
<p>Graur et. al. quote François Jacob, writing in 2007:</p>
<blockquote>
	<p>[N]atural selection does not work as an engineer… It works like a tinkerer—a tinkerer&nbsp; who does not know exactly what he is going to produce but uses whatever he finds around
		him whether it be pieces of string, fragments of wood, or old cardboards… The tinkerer…
		manages with odds and ends. What he ultimately produces is generally related to
		no special project, and it results from a series of contingent events, of all the opportunities
		he had to enrich his stock with leftovers.</p>
</blockquote>
<h2>Appears to do Nothing</h2>
<blockquote>
	<p>The human genome is populated by a very large number of transposable and mobile elements.
		Transposable elements, such as LINEs, SINEs, retroviruses, and DNA transposons,
		may, in fact, account for up to two thirds of the human genome</p>
</blockquote>
<h2>C-Value Paradox</h2>
<p>They quote a personal communication from T. Ryan Gregory:</p>
<blockquote>
	<p>The onion test is a simple reality check for anyone who thinks they can assign
		a function to every nucleotide in the human genome.&nbsp; Whatever your proposed
		functions are, ask yourself this question: Why does an onion need a genome
		that is about five times larger than ours?</p>
</blockquote>
<h2>How much is junk?</h2>
<blockquote>
	<p>the proportion of the human genome that is functional is likely to be larger
		to some extent than the approximately 9% for which there exists some evidence
		for selection, but the fraction is unlikely to be anything even approaching
		80%....
	</p>
	<p>
		<br>
	</p>
	<p>Ward and Kellis (2012) confirmed that ~5% of the genome is interspecifically conserved,
		and by using intraspecific variation, found evidence of lineage-specific constraint
		suggesting that an additional 4% of the human genome is under selection (i.e.,
		functional), bringin the total fraction of the genome that is certain to be functional
		to approximately 9%.</p>
</blockquote>
<p>At the end, Graur etl al. tell us their arguments are really only in opposition to ID
	proponents:
</p>
<blockquote>
	<p>We urge biologists not be afraid of junk DNA. The only people that should be afraid
		are those claiming that natural processes are insufficient to explain life
		and that evolutionary theory should be supplemented or supplanted by an intelligent
		designer (e.g., Dembski 1998; Wells 2004)</p>
</blockquote>
<h2>Criticism</h2>
<p>Bradley Bernstein, an ENCODE researcher at Harvard University <a href="http://ge.tt/4DHgamX1/v/0?c"
	>remarked in Science</a> that "Graur wrote such a negative paper that it
	was hard to read... Graur’s criticism is so over-the-top that it’s not worthy."</p>
<p>On reddit, an ENCODE researcher <a href="http://www.reddit.com/r/askscience/comments/1l3zfx/how_come_theres_a_amoeba_with_200_times_larger/cbw1bze"
	>pointed out</a> [<a href="http://archive.is/21x8y">archive.is mirror</a>]
	that Graur was incorrect in some of his accusations, such as ENCODE using HeLa cells
	(cancerous cells that continue to replicate outside humans) as tests for transcription: </p>
<blockquote>
	<p>The vast majority of the RNAseq was performed in GM12878 or K562 (but we looked
		at a lot of different cell types, <a href="http://genome.ucsc.edu/ENCODE/cellTypes.html"
		>see here.</a>.. That assertion is completely false and I don't want
		that misinformation being spread... We used K562 because it is transcriptionally
		normal
	</p>
</blockquote>
