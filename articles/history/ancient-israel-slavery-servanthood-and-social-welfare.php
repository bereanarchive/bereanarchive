<h3>Laws to protect Israelite servants</h3>
<ol>
	<li>"If any of your kin fall into difficulty and become dependent on you, you shall
		support them; they shall live with you as though resident aliens. Do not take interest
		in advance or otherwise make a profit from them, but fear your God; let them live
		with you. You shall not lend them your money at interest taken in advance, or provide
		them food at a profit... You shall not rule over them with harshness, but shall
		fear your God." (Lev 25:35-43)</li>
	<li>Lifelong servitude of Israelites was forbidden.&nbsp; They must be released after
		7 years without debt and their family units must be maintained unless the servant
		agreed to do otherwise. (Ex 21:2-5, Deut 15:12, Lev 25:40-41, Jer 34:14)</li>
	<li>When they failed to release Israelites after 7 years, the Lord promised them 
		"to the sword, to pestilence, and to famine. I will make you a horror to all the 
		kingdoms of the earth."&nbsp; And "Their corpses shall become food for the birds of the air 
		and the wild animals of the earth." (Jer 34:17-20)
	<li>Capturing Israelites to be slaves during civil war was forbidden (2 Chron 28:10-11)</li>
	<li>After releasing a servant, they must be sent away with generous supplies. (Deut
		15:13-14)
	</li>
	<li>If a servant declared "he loves you and your household, since he is well off with
		you" (Deut 15:16-17) he can chose to stay for life, presumably to allow elderly
		or injured servants security in their old age.</li>
</ol>
<p></p>
<h3>Laws to protect both foreign and Israelite servants</h3>
<ol>
	<li>Servant/slaves were required to have a day off once a week on the Sabbath. (Ex 23:12,
		Deut 5:14)</li>
	<li>"Slaves who have escaped to you from their owners shall not be given back to them.&nbsp; They
		shall reside with you, in your midst, in any place they choose in any one of your
		towns, wherever they please; you shall not oppress them." (Deut 23:15-16)
		Although there's debate about whether this meant only a slave that escapes to Israel from a foreign nation.[^barnes-1830][^copan-2011]

	</li>
	<li>Servant/slaves were not to be slandered (Prov 30:10).</li>
	<li>Kidnapping people to be servant/slaves was punishable by death (Ex 21:16).</li>
	<li>Any servant/slave who received a permanent injury was to be freed
		(Ex 21:26-27).&nbsp; Any masters who killed a servant/slave was to be punished
		(Ex 21:20), presumably in the way of Ex 21:12, "Whoever strikes a person mortally
		shall be put to death".</li>
	<li>It was a sin for a master to sleep with a female servant who was pledged to someone
		else. He must pay her compensation but she is not considered guilty (Lev 19:20-21).
		&nbsp;Israelite Sexual relations foreigners who followed other gods was forbidden
		outright (Num 25:1, 5) unless they married them (Deut 21:10-15).</li>
	<li>Children of foreign servant/slaves may have become part of the nation
		of Israel and fell under the native Israelite laws, since heritage seemed to be
		determined by the household you were born in (Gen 17:12-13, Lev. 22:11).</li>
	<li>Servant/slaves could likely own property (2 Sam 16:4).</li>
	<li>Servant/slaves could inherit their master's household if there
		was no other heir (Gen 15:3) and perhaps even if they are more worthy than the
		true heirs (Prov 17:2).</li>
</ol>
<p></p>
<p>However, foreigner servant/slaves were not released after 7 years (Lev 25:46, Joshua
	9:27).</p>
