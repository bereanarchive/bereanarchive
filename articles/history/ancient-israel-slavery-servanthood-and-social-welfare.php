<h2>Nature of Servitude in Ancient Israel</h2>
<h3>Laws to safeguard against poverty and prevent indentured servitude</h3>
<aside>
	<img src="ancient-israel-slavery-servanthood-and-social-welfare-files/harold-copping-ruth-1923.jpg?w=230"
	class="rounded" style="width: 100%;">
	<p>Ruth meets Boaz.&nbsp; Painting by Harold Copping, 1923.</p>
	<p>In Ruth 2:7, Ruth gathered about 21 liters of barley in a single day by gleaning
		a field.</p>
</aside>
<p>In ancient Israel, indentured servitude was a consequence of poverty.&nbsp; If "any
	of your kin fall into difficulty... and sell themselves" (Lev 25:47.)&nbsp; However,
	many safeguards existed to prevent poverty from happening at all:</p>
<ol>
	<li>Israelites were required to lend to their fellow countrymen in poverty,
		"If there is among you anyone in need... you should rather open your hand, willingly
		lending enough to meet the need, whatever it may be.", otherwise "your neighbor
		might cry to the Lord against you, and you would incur guilt." (Deut 15:7-9)</li>
	<li>When harvesting from the field extra food must be left for the poor:&nbsp; "you
		shall not reap to the very edges of your field, or gather the gleanings of your
		harvest... you shall leave them for the poor and the alien" (Lev 19:9-10).
		&nbsp;This was an effective charity.&nbsp; In Ruth 2:7, Ruth requests "Please,
		let me glean and gather among the sheaves behind the reapers.’" and in one day
		she was able to gather "about an ephah [21.14 liters] of barley." (2:17)</li>
	<li>Creditors could not charge interest, or keep vital supplies such as blankets during
		cold nights. (Ex 22:25-27)</li>
	<li>Workers were to be paid the same day of their service. (Lev 19:13)</li>
	<li>It was required that the poor not be ignored in complaints of oppression:&nbsp; "You
		shall not pervert the justice due to your poor in their lawsuits." (Ex 23:6).&nbsp; "Defend
		the rights of the poor and needy." (Prov 31:9)</li>
	<li>Helping the poor was considered a means to know God: "He judged the cause of the
		poor and needy; then it was well.&nbsp; Is not this to know me? says
		the Lord." (Jer 22:16)</li>
	<li>Blessings were promised to the generous: "Some give freely, yet grow all the richer;
		<br>others withhold what is due, and only suffer want" (Prov 11:24).&nbsp; "Whoever
		is kind to the poor lends to the Lord, and will be repaid in full" (Prov 19:17).
		"Whoever gives to the poor will lack nothing, but one who turns a blind eye
		will get many a curse."&nbsp; (Prov 28:27)</li>
	<li>Anyone who injures someone else in a fight must compensate them for loss
		of time and provide care for them until they are healed. (Ex 21:19)</li>
	<li>Family members were to help each other repurchase their land if they fell into debt
		and lost it. (Lev 25:23-34)</li>
	<li>Women who became widows with no male heirs had the right to gain an heir by their
		husbands’ brothers, thereby securing a son to care for them in their old age. (Deut
		25:5-10)
	</li>
	<li>God promised to kill by the sword anyone who hurt widows or orphans. (Ex 22:22-24)</li>
	<li>Foreigners, orphans, and widows were given a tenth of the annual produce once every
		three years. (Deut 14:28)</li>
	<li>The year of jubilee (once every 50 years) was a comprehensive program of debt cancellation
		and the complete restoration of each family’s ancestral property, which granted
		the poor a fresh start. (Lev 25:8-22)</li>
</ol>
<p>Israel was told that if these failsafes were followed, "There will... be no
	one in need among you" (Deut 15:4).&nbsp; Without poverty there was no need for
	the destitute to sell themselves into slavery.&nbsp; Later, God casts judgment on
	Israel for their failure to care for the poor:</p>
<ol>
	<li>Amos 2:6-7: Thus says the Lord:&nbsp; For three transgressions of Israel, and
		for four, I will not revoke the punishment; because they sell the righteous
		for silver, and the needy for a pair of sandals— they who trample the
		head of the poor into the dust of the earth, and push the afflicted out of
		the way"</li>
</ol>
<h3>Laws to protect foreigners</h3>
<ol>
	<li>"You shall not oppress a resident alien; you know the heart of an alien, for you
		were aliens in the land of Egypt" (Ex 23:9).&nbsp; " You shall not withhold the
		wages of poor and needy laborers, whether other Israelites or aliens who reside
		in your land in one of your towns." (Deut 24:14)</li>
	<li>"The alien who resides with you shall be to you as the citizen among you; you shall
		love the alien as yourself" (Lev 19:34).&nbsp; " You shall also love the stranger,
		for you were strangers in the land of Egypt" (Deut 10:19)</li>
	<li>Judges were required to give equal treatment to foreigners: "Give the members of
		your community a fair hearing, and judge rightly between one person and another,
		whether citizen or resident alien" (Deut 1:16).&nbsp; "You shall have one law for
		the alien and for the citizen." (Lev 24:22)</li>
	<li>Part of the harvest each year was to be left in the fields for foreigners to collect
		for themselves: "you shall leave them for the poor and for the alien" (Lev 23:22).</li>
	<li>Foreigners, orphans, and widows were given a tenth of the annual produce once every
		three years. (Deut 14:28)</li>
	<li>Foreigners were legally allowed to own Israelites as servants. (Lev 25:47)</li>
</ol>
