<h2>Servitude in Ancient Israel was far better than other ancient Near East civilizations</h2>
<p>This is not to argue that being relatively better than other ANE civilizations is
	a justification for slavery/servanthood in the Old Testament in itself.&nbsp; See
	the arguments above for that.</p>

<p>The Anchor Bible Dictionary:</p>
<blockquote>
	We have in the Bible the first appeals in world literature to treat slaves as human
	beings for their own sake and not just in the interests of their masters... In contrast
	to many ancient doctrines, the Hebrew law was relatively mild toward the slaves
	and recognized them as human beings subject to defense from intolerable acts, although
	not to the same extent as free persons.[^Anchor-page-8255]
</blockquote>

<p>Historian Paul Johnson:</p>
<blockquote>
	These dreadful laws [of Hammurabi] are notable for the ferocity of their physical
	punishments, in contrast to the restraint of the Mosaic Code and the enactments
	of Deuteronomy and Leviticus, which sprang from it.[^Johnson-2003]</blockquote>

<p>Christian theologian Robin A Perry wrote in 2004:</p>
<blockquote>
	Consider the centrality of property offences in the Laws of Hammurabi.&nbsp; Despite
	the fact that the laws in the "Book of the Covenant" are very similar to these laws,
	they clearly regard crimes against the person as far more serious than property
	offences.[^Perry-2004]

</blockquote>

<p>Old Testament ethicist Christopher Wright wrote in 2013:</p>
<blockquote>
	Three Old Testament civil laws are quite unparalleled in any other ancient Near Eastern
	code.&nbsp; Exodus 21:20-21 and 21:26-27 take up the case of slaves injured or killed
	by their own masters, and Deuteronomy 23:15-16 grants asylum to a runaway slave.
	&nbsp;No other ancient near Eastern law has been found that holds a master to account
	for the treatment of his own slaves (as distinct from injury done to the slave of
	another master), and the otherwise universal law regarding runaway slaves was that
	they must be sent back, with severe penalties for those who failed to comply.[^Wright-2013]</blockquote>

