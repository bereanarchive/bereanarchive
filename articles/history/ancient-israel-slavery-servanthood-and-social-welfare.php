<h1>Ancient Israel:&nbsp; Slavery, Servanthood, and Social Welfare</h1>
<h2>Overview</h2>
<p>This article corrects the common misconception that slavery/servanthood
	in the legal code of ancient Israel was comparable to the harsh slavery of other
	ancient cultures and the early United States.&nbsp; Rather, in ancient Israel the
	institution served as protection against poverty and included numerous safeguards
	against abuse. As well as a means of integrating the Caananites Isreal defeated and captured
	because of their sexual abuse and killing of children (Lev 18:21-30, Deut 12:31, Deut 18:9-10, Psalm 106:35-38, 2 Chron 28:3).
	Three main points:</p>
