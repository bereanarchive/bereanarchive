<h2>Common Objections</h2>
<h3>Objection 1:&nbsp; "Exodus 21:7-11 allows selling daughters into sexual slavery"</h3>
<p>Does this passage say that "every man is free to sell his daughter into sexual
	slavery" as Sam Harris argues?[^Harris-2006-page-14]&nbsp; Exodus 21:7-11:</p>
<blockquote>When a man sells his daughter as a slave, she shall not go out as the
	male slaves do.&nbsp; If she does not please her master, who designated her for
	himself, then he shall let her be redeemed; he shall have no right to sell her to
	a foreign people, since he has dealt unfairly with her.&nbsp; If he designates her
	for his son, he shall deal with her as with a daughter.&nbsp; If he takes another
	wife to himself, he shall not diminish the food, clothing, or marital rights of
	the first wife.&nbsp; And if he does not do these three things for her, she shall
	go out without debt, without payment of money.</blockquote>
<p>Is this prostutution or an agreement of servitute/arranged marriage?&nbsp; Prostitution
	was forbidden by Lev 19:29, "Do not profane your daughter by making her a prostitute,
	that the land not become prostituted and full of depravity."&nbsp; The word for
	prostitute is Hebrew האבת, (zanah) which is defined as "to commit fornication,
	be a harlot, play the harlot".[^zanah]&nbsp; However the word for "slave" in Exodus
	21:7 is Hebrew אָמָה (’amah) which refers to a female handmaid who could become
	a concubine or wife.[^amah] "If he designates her for his son" indicates this was
	an arranged marriage, and the financial circumstances of a man selling his daughter
	suggest this was a plan to get the daughter into a better lifestyle than the poverty
	of her parents.&nbsp; </p>
<p>Most importantly, verse 11 says she's free to leave if she's not adequately cared
	for.
</p>
<h3>Objection 2:&nbsp; "Exodus 21:20-21 allows physically abusing slaves"</h3>
<p>Does this give a master free reign to beat his servant as much as he wants so long
	as he doesn't kill him?</p>
<blockquote>
	<p>When a slaveowner strikes a male or female slave with a rod and the slave dies immediately,
		the owner shall be punished.&nbsp; But if the slave survives a day or two, there
		is no punishment; for the slave is the owner’s property.</p>
</blockquote>
<p>Critics often claim this verse gives slave/servant-masters the right to unjust
	physical abuse. But if every command in the Old Testament is followed, it's impossible
	to treat a servant-slave (Hebrew uses the same word for both) in an inhumane
	way. Imagine two contexts in which Exodus 21:20-21 can be taken:
</p>
<ol>
	<li><b>Interpretation 1:</b>&nbsp; Israelites used it as a license for physical
		abuse against their servant/slaves, for any reason at all, up to but not including
		death.
	</li>
	<li><b>Interpretation 2:</b>&nbsp; It was a regulation governing
		physical punishment in response to crimes, possibly such as theft, assault, or
		attempted child sacrifice (popular among the foreigners taken as servant/slaves,
		Lev 18:21, Deut 12:31).</li>
</ol>
<p>Acting in terms of the first interpretation violates many other commands in the Old
	Testament:
</p>
<ol>
	<li>"You must not oppress a foreigner" Ex 23:9</li>
	<li>"Do not seek revenge or bear a grudge against anyone among your people, but love
		your neighbor as yourself" (Lev 19:18)</li>
	<li>"The foreigner who resides with you must be to you like a native citizen among you;
		so you must love him as yourself." (Lev 19:34)</li>
	<li>"you must love the resident foreigner." (Deut 10:19)</li>
	<li>"Learn to do what is right!&nbsp; Promote justice!&nbsp; Give the oppressed reason
		to celebrate!&nbsp; Take up the cause of the orphan!&nbsp; Defend the rights of
		the widow!" (Isaiah 1:17)</li>
</ol>
<p>And many more above. Therefore since interpretation #1 is contradictory, it cannot
	be correct and we should prefer interpretation #2.&nbsp; Otherwise it must be argued that
	beating a servant/slave without just cause is still loving him (Lev 19:34,38),
	promoting justice, and fighting oppression (Ex 23:8, Isaiah 1:17)!
</p>
<h4>A Hypothetical Scenario</h4>
<aside>
	<img src="ancient-israel-slavery-servanthood-and-social-welfare-files/faris-sacrificing-to-baal-1891.jpg?w=230" class="rounded" style="width: 100%;">
	<p>Sacrificing to Baal. By Habib Faris, 1891.&nbsp; Colorized using <a href="http://colorize.ttic.edu/">Automatic Colorizer</a>.</p>
	<p>Deut 12:31 says the Canaanites "would even burn their sons and their daughters in the fire to their gods."
	</p>
</aside>
<p>The nations of Canaan had carried out incest with children/grandchildren and performed
	child sacrifice by fire (Lev 18:6-30, Deut 12:31, Deut 18:9-10, Psalm 106:35-38,
	2 Chron 28:3).&nbsp; Israel was commanded to fight against them and take them as
	servant/slaves to end these atrocities (Lev 25:44).&nbsp; Suppose a captured Canaanite
	was found about to throw his infant daughter into a raging fire as a sacrifice to
	Molech. With no prison system in place, physical beating seems more than appropriate
	to deter such heinous acts from ever happening again.</p>
<p>Finally, if the punishment causes any permanent bodily damage, the servant is free
	to go with any debt erased (Ex 21:26-27).&nbsp; Any master who killed a servant/slave were
	to be punished (Ex 21:20), presumably in the way of Ex 21:12, "Whoever strikes a
	person mortally shall be put to death."&nbsp; A master that beat a servant/slave
	within an inch of his life was within an inch of his own life as well.</p>
<p>If discipline was too harsh the servant/slave could simply run away,
	live wherever they chose, and it was illegal to make them go back:&nbsp; "Slaves
	who have escaped to you from their owners shall not be given back to them. They
	shall reside with you, in your midst, in any place they choose in any one of your
	towns, wherever they please; you shall not oppress them." (Deut 23:15-16)
</p>
<h3>Objection 3:&nbsp; "Leviticus 25:44-46 allows perpetual slaves"</h3>
<blockquote>As for the male and female slaves whom you may have, it is from the nations around
	you that you may acquire male and female slaves.&nbsp; You may also acquire them
	from among the aliens residing with you, and from their families that are with you,
	who have been born in your land; and they may be your property.&nbsp; You may keep
	them as a possession for your children after you, for them to inherit as property.
	These you may treat as slaves, but as for your fellow Israelites, no one shall rule
	over the other with harshness.</blockquote>
<p>The nations of Canaan had carried out incest with children/grandchildren and performed
	child sacrifice by fire (Lev 18:21-30, Deut 12:31, Deut 18:9-10, Psalm 106:35-38).
	Removing the children from these situations was a moral imperative.&nbsp; Commands
	like Lev 19:34 guarded against wrongdoing: "The foreigner who resides with you must
	be to you like a native citizen among you; so you must love him as yourself".</p>
<p>It's also possible Deut 23:15-16 might come into play here, supposing
	that it is not meant only for slaves escaped from other nations.[^barnes-1830][^copan-2011]
	If so, then under unjust treatment the slave/servant can run away, live wherever they chose, and it was illegal to make them go
	back.&nbsp; But doesn't "you may enslave them perpetually" contradict this?&nbsp; In
	terms of a contract, marriage is intended to last until death.&nbsp; But there
	are still regulations for what should happen if that contract fails, and this interpretation
	of the "escape clause" seems the most parsimonious in resolving these two commands.</p>
