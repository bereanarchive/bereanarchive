<h3>Objection 5:&nbsp; "Slavery is morally wrong in any circumstance"</h3>
<p>Given that Hebrew עֶבֶד (ebed) can mean a subordinate of any priveledge, and
	that the other Old Testament comamnds make it impossible for masters to
	treat Israelite or foreign servant-slaves inhumanely, it makes it not only difficult,
	but ethnocentric to equate it with the modern English connotation of slavery.</p>
<p>Moreso, this objection itself is misguided because it assumes morality can be objectively
	defined apart from God.&nbsp; Suppose we are the product of evolution alone, and
	evolution programmed our minds to think that rape and murder were moral
	acts. We would then condemn the Old Testament for its rejection of them.&nbsp; Perhaps
	evolution even produced such a conviction very strongly.&nbsp; But God does not
	exist, there is no objective means to define in which morality we are intended
	to operate.&nbsp; And one viewof morality is no more right or wrong than any
	other.&nbsp; Wll morality woudl be no more than personal preference.&nbsp; </p>
<p>But if God designed us for a particular purpose, then morality is merely what God
	intends for us to do.&nbsp; Since it is a contradiction to freely violate ones own
	intentions, it is impossible for God intentions (as described in the Old Testament
	law) to be immoral.</p>
