<h2>Servitude in Ancient Israel was unlike early U.S. chattel slavery</h2>
<h3>Not the same as slavery in Ancient Israel</h3>
<aside>
	<img src="ancient-israel-slavery-servanthood-and-social-welfare-files/uncle-toms-cabin.jpg?w=230"
	style="width: 100%;">
</aside>
<p>In 1853, the well-known abolitionist Harriet Beecher Stowe wrote:</p>
<blockquote>
	The legal power of the master amounts to an absolute despotism over body and soul,
	and there is no protection for the slave’s life or limb, his family relations, his
	conscious, nay more, his eternal interests.[^Stowe-1853]
</blockquote>
<p></p>
<p>Likewise, Yale's Anchor Bible Dictionary helps tells us the word translated
	as "slave" had a much broader definition than the word we use today:</p>
<blockquote>
	The usual term for "slave" in Mesopotamia was wardum, which at the same time was
	used not only to designate an actual slave but also dependence in the broad sense
	or servility, thus corresponding to Heb <em>ebed</em>, Aram <em>abda</em>,
	Old Pers <em>bandaka</em>, Gk <em>doulos</em>, etc. In the ANE [Ancient Near East],
	all the subjects of the king, including even highly placed officials, were regarded
	as slaves of the king.[^Anchor-page-8246]
</blockquote>
<p></p>
<p>Passages such as Deuteronomy 15:16-17 support the view that (if all regulations were
	followed) servant/slavery in the Old Testament could serve the best interest of
	the servant/slave, presumably by providing security and economic well-being:</p>
<blockquote>But if he says to you, “I will not go out from you,” because he loves you and your
	household, since he is well off with you, then you shall take an awl and thrust
	it through his earlobe into the door, and he shall be your slave forever.&nbsp; You
	shall do the same with regard to your female slave.</blockquote>
<p></p>
<p>Consider how different slavery would have been in the early United States even
	if only a small number of the Old Testament regulations were kept:
</p>
<ol>
	<li>Servant/slaves go free if they suffer permanent injuries. (Ex 21:26-27)</li>
	<li>Kidnapping people to be servant/slaves was punishable by death. (Ex 21:16).&nbsp; But
		slavery in the U.S. south started from kidnapping.</li>
	<li>If a servant/slave ran away, they would not have to return and could live anywhere
		they chose. (Deut 23:15)</li>
	<li>If slavery were not based on race but instead was a means to avoid destitution in
		poverty.
	</li>
</ol>
